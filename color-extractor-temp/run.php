function html2rgb( $color )
{

    if ( $color[0] == '#' )
    {
        $color = substr( $color, 1 );
    }

    if ( strlen( $color ) == 6 )
    {
        list( $r, $g, $b ) = array(
            $color[0].$color[1],
            $color[2].$color[3],
            $color[4].$color[5]
        );
    }
    elseif ( strlen( $color ) == 3 )
    {
        list( $r, $g, $b ) = array(
            $color[0].$color[0],
            $color[1].$color[1], $color[2].$color[2]
        );
    }
    else
    {
        return false;
    }

    $r = hexdec( $r );
    $g = hexdec( $g );
    $b = hexdec( $b );

    return array( $r, $g, $b );
}

function distancel2( array $color1, array $color2 )
{
    return sqrt( pow( $color1[0] - $color2[0], 2 ) +
        pow( $color1[1] - $color2[1], 2 ) +
        pow( $color1[2] - $color2[2], 2 ) );
}

function generate_csv( $data )
{
    $f = fopen( 'imagesColors.csv', 'w' );

    if ( $f === false )
    {
        die( 'Error opening the file imagesColors.csv' );
    }

// write each row at a time to a file
    foreach ( $data as $row )
    {
        fputcsv( $f, $row );
    }

    // close the file
    fclose( $f );
}

function check_if_already_processed( $img )
{
    $found = false;
    $data  = array_map( 'str_getcsv', file( 'imagesColors.csv' ) );

    if ( ! empty( $data ) )
    {

        foreach ( $data as $row )
        {

            if ( $row[0] == $img )
            {
                $found = true;
                break;
            }

        }

    }

    return $found;
}

